package oop.static4;

public class Robot {

	public static double multi(double m1,double m2) {
		return m1*m2;
	}
	public static double square(int s) {
		return s*s;
	}
	public static double cir(int c) {
		return c*c*3.14;
	}
	public static double tri(double t1, double t2){
		return t1*t2/2;
	}

}
